/// Integration tests for backend-rust.
///
/// Each test compiles a CDDL snippet to IR then runs the Rust backend,
/// asserting on the generated source text.

use std::path::PathBuf;
use backend_rust::RustBackend;
use cddlc_codegen::{Backend, CodegenOptions};
use cddlc_ir::lower;
use cddlc_parser::parse_cddl;

fn generate(src: &str) -> String {
    let parsed  = parse_cddl(src, PathBuf::from("test.cddl")).expect("parse");
    let lowered = lower(&parsed.value, 16, 64).expect("lower");
    let opts    = CodegenOptions::default();
    let out     = RustBackend.generate(&lowered.module, &opts).expect("generate");
    out.files[1].content.clone()
}

fn generate_no_std(src: &str) -> String {
    let parsed  = parse_cddl(src, PathBuf::from("test.cddl")).expect("parse");
    let lowered = lower(&parsed.value, 16, 64).expect("lower");
    let mut opts = CodegenOptions::default();
    opts.no_std = true;
    let out = RustBackend.generate(&lowered.module, &opts).expect("generate");
    out.files[1].content.clone()
}

// ── Smoke tests ───────────────────────────────────────────────────────────────

#[test]
fn test_generates_header() {
    let code = generate("device-id = uint");
    assert!(code.contains("@generated by cddlc"));
    assert!(code.contains("use minicbor"));
}

#[test]
fn test_no_std_header() {
    let code = generate_no_std("device-id = uint");
    assert!(code.contains("cfg_attr") && code.contains("no_std"));
}

// ── Type alias ────────────────────────────────────────────────────────────────

#[test]
fn test_simple_alias() {
    let code = generate("device-id = uint");
    assert!(code.contains("pub type DeviceId = u64;"));
}

#[test]
fn test_tstr_alias() {
    let code = generate("label = tstr");
    assert!(code.contains("pub type Label = String;"));
}

#[test]
fn test_constrained_alias_newtype() {
    let code = generate("device-id = tstr .size 16");
    // Constrained alias → newtype struct
    assert!(code.contains("pub struct DeviceId"));
    assert!(code.contains("impl<'b> Decode<'b, ()> for DeviceId"));
}

#[test]
fn test_constraint_check_in_decode() {
    let code = generate("device-id = tstr .size 16");
    assert!(code.contains("val.len() == 16"));
}

#[test]
fn test_range_constraint_check() {
    let code = generate("temp = int .ge -40 .le 85");
    assert!(code.contains("pub struct Temp"));
    assert!(code.contains("-40"));
    assert!(code.contains("85"));
}

