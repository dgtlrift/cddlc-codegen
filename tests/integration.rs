// ── Struct ────────────────────────────────────────────────────────────────────

#[test]
fn test_struct_decl() {
    let code = generate("sensor = { id: uint, label: tstr, value: float32 }");
    assert!(code.contains("pub struct Sensor {"));
    assert!(code.contains("pub id: u64,"));
    assert!(code.contains("pub label: String,"));
    assert!(code.contains("pub value: f32,"));
}

#[test]
fn test_struct_encode_impl() {
    let code = generate("sensor = { id: uint, value: float32 }");
    assert!(code.contains("impl<C> Encode<C> for Sensor"));
    assert!(code.contains("e.map(2u64)?"));
}

#[test]
fn test_struct_decode_impl() {
    let code = generate("sensor = { id: uint, value: float32 }");
    assert!(code.contains("impl<'b> Decode<'b, ()> for Sensor"));
    assert!(code.contains("let key = d.str()?"));
    assert!(code.contains("\"id\""));
}

#[test]
fn test_optional_field_is_option() {
    let code = generate("msg = { id: uint, ? label: tstr }");
    assert!(code.contains("pub label: Option<String>,"));
}

#[test]
fn test_required_field_missing_errors() {
    let code = generate("msg = { id: uint }");
    assert!(code.contains("missing field"));
}

#[test]
fn test_unknown_field_skipped() {
    let code = generate("msg = { id: uint }");
    // Unknown keys should be skipped (permissive)
    assert!(code.contains("d.skip()?"));
}

#[test]
fn test_struct_with_ref() {
    let code = generate(r#"
device-id = tstr
sensor = { id: device-id, value: float32 }
"#);
    assert!(code.contains("pub id: DeviceId,"));
}

#[test]
fn test_tagged_struct() {
    let code = generate("tagged-sensor = #6.100({ id: uint, value: float32 })");
    assert!(code.contains("Tag::new(100)"));
    assert!(code.contains("pub struct TaggedSensor"));
}

